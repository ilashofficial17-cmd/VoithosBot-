def detect_lang(user) -> str:
    """Detect language from Telegram user's language_code."""
    code = (user.language_code or "")[:2].lower()
    if code == "de":
        return "de"
    if code == "ru":
        return "ru"
    return "en"


def t(context, key: str) -> str:
    """Get translated text for current user's language."""
    lang = context.user_data.get("lang", "ru")
    texts = TEXTS.get(lang) or TEXTS["ru"]
    # Fallback to Russian if key missing in current language
    return texts.get(key) or TEXTS["ru"].get(key, key)
